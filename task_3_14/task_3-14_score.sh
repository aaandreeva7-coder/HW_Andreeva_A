#!/bin/bash

FILE="students.txt"

echo "========================================="
echo "         АНАЛИЗ УСПЕВАЕМОСТИ"
echo "========================================="
echo ""


if [ ! -f "$FILE" ]; then
    echo "❌ Ошибка: Файл $FILE не найден!"
    echo "   Сначала создайте файл students.txt с данными студентов."
    exit 1
fi


echo "🏆 1. Студенты с оценкой ВЫШЕ 80 баллов:"
echo "----------------------------------------"
awk '$2 > 80 {print "   " $1 " - " $2 " баллов"}' "$FILE"


echo ""
echo "========================================="
echo ""


echo "📉 2. Студенты с оценкой НИЖЕ 70 баллов:"
echo "----------------------------------------"
awk '$2 < 70 {print "   " $1 " - " $2 " баллов"}' "$FILE"


echo ""
echo "========================================="
echo ""

echo "📄 3. Первая строка файла:"
echo "----------------------------------------"
head -1 "$FILE" | awk '{print "   Имя: " $1 ", Оценка: " $2}'


echo ""
echo "========================================="
